package model;

public class RPhoto {
	private int rp_no;
	private int rm_no;
	private String rp_name;
	
	
	public int getRp_no() {
		return rp_no;
	}
	public void setRp_no(int rp_no) {
		this.rp_no = rp_no;
	}
	public int getRm_no() {
		return rm_no;
	}
	public void setRm_no(int rm_no) {
		this.rm_no = rm_no;
	}
	public String getRp_name() {
		return rp_name;
	}
	public void setRp_name(String rp_name) {
		this.rp_name = rp_name;
	}
	

	
	
}
